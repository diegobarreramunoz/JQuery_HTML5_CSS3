S2.define('select2/selection/placeholder',[
  '../utils'
], function (Utils) {
  function Placeholder (decorated, jqelement, options) {
    this.placeholder = this.normalizePlaceholder(options.get('placeholder'));

    decorated.call(this, jqelement, options);
  }

  Placeholder.prototype.normalizePlaceholder = function (_, placeholder) {
    if (typeof placeholder === 'string') {
      placeholder = {
        id: '',
        text: placeholder
      };
    }

    return placeholder;
  };

  Placeholder.prototype.createPlaceholder = function (decorated, placeholder) {
    var jqplaceholder = this.selectionContainer();

    jqplaceholder.html(this.display(placeholder));
    jqplaceholder.addClass('select2-selection__placeholder')
                .removeClass('select2-selection__choice');

    return jqplaceholder;
  };

  Placeholder.prototype.update = function (decorated, data) {
    var singlePlaceholder = (
      data.length == 1 && data[0].id != this.placeholder.id
    );
    var multipleSelections = data.length > 1;

    if (multipleSelections || singlePlaceholder) {
      return decorated.call(this, data);
    }

    this.clear();

    var jqplaceholder = this.createPlaceholder(this.placeholder);

    this.jqselection.find('.select2-selection__rendered').append(jqplaceholder);
  };

  return Placeholder;
});

S2.define('select2/selection/allowClear',[
  'jquery',
  '../keys'
], function (jq, KEYS) {
  function AllowClear () { }

  AllowClear.prototype.bind = function (decorated, container, jqcontainer) {
    var self = this;

    decorated.call(this, container, jqcontainer);

    if (this.placeholder == null) {
      if (this.options.get('debug') && window.console && console.error) {
        console.error(
          'Select2: The `allowClear` option should be used in combination ' +
          'with the `placeholder` option.'
        );
      }
    }

    this.jqselection.on('mousedown', '.select2-selection__clear',
      function (evt) {
        self._handleClear(evt);
    });

    container.on('keypress', function (evt) {
      self._handleKeyboardClear(evt, container);
    });
  };

  AllowClear.prototype._handleClear = function (_, evt) {
    // Ignore the event if it is disabled
    if (this.options.get('disabled')) {
      return;
    }

    var jqclear = this.jqselection.find('.select2-selection__clear');

    // Ignore the event if nothing has been selected
    if (jqclear.length === 0) {
      return;
    }

    evt.stopPropagation();

    var data = jqclear.data('data');

    for (var d = 0; d < data.length; d++) {
      var unselectData = {
        data: data[d]
      };

      // Trigger the `unselect` event, so people can prevent it from being
      // cleared.
      this.trigger('unselect', unselectData);

      // If the event was prevented, don't clear it out.
      if (unselectData.prevented) {
        return;
      }
    }

    this.jqelement.val(this.placeholder.id).trigger('change');

    this.trigger('toggle', {});
  };

  AllowClear.prototype._handleKeyboardClear = function (_, evt, container) {
    if (container.isOpen()) {
      return;
    }

    if (evt.which == KEYS.DELETE || evt.which == KEYS.BACKSPACE) {
      this._handleClear(evt);
    }
  };

  AllowClear.prototype.update = function (decorated, data) {
    decorated.call(this, data);

    if (this.jqselection.find('.select2-selection__placeholder').length > 0 ||
        data.length === 0) {
      return;
    }

    var jqremove = jq(
      '<span class="select2-selection__clear">' +
        '&times;' +
      '</span>'
    );
    jqremove.data('data', data);

    this.jqselection.find('.select2-selection__rendered').prepend(jqremove);
  };

  return AllowClear;
});

